function testPropertyFuzz() {
  const statuses = ["not_started", "in_progress", "blocked", "done", undefined, null]

  for (let i = 0; i < 100; i++) {
    const title = i % 10 === 0 ? undefined : randomString(Math.floor(Math.random() * 30))
    const status = statuses[Math.floor(Math.random() * statuses.length)]
    const summary = i % 7 === 0 ? null : randomString(Math.floor(Math.random() * 50))
    const planLen = Math.floor(Math.random() * 20)
    const plan = i % 5 === 0 ? null : randomArray(planLen)

    const result = deepMerge(null, {
      task: { title, status },
      summary,
      currentPlan: plan,
    })

    assert(`fuzz[${i}]: result defined`, result !== null && result !== undefined)
    assert(`fuzz[${i}]: version is ${SCHEMA_VERSION}`, result.version === SCHEMA_VERSION)
    assert(`fuzz[${i}]: updatedAt is string`, typeof result.updatedAt === "string")

    if (title !== undefined && title !== null) {
      assert(`fuzz[${i}]: title preserved`, result.task.title === title)
    }

    // Deep merge with incoming
    const title2 = randomString(10)
    const result2 = deepMerge(result, { task: { title: title2 } })
    assert(`fuzz[${i}]: deep merge overwrites title`, result2.task.title === title2)
    if (summary !== undefined && summary !== null) {
      assert(`fuzz[${i}]: deep merge keeps summary`, result2.summary === summary)
    }
  }
}

async function tmpDir() {
  const dir = join(tmpdir(), "cp-comp-" + Date.now() + "-" + Math.random().toString(36).slice(2))
  await mkdir(dir, { recursive: true })
  return dir
}

function randomString(len) {
  let s = ""
  const chars = "abcABC123 \t\n!@#$%^&*()_+-=[]{}|;':\",./<>?~`" + "\\"
  for (let i = 0; i < len; i++) s += chars[Math.floor(Math.random() * chars.length)]
  return s
}

function randomArray(len) {
  const arr = []
  for (let i = 0; i < len; i++) arr.push(randomString(1 + Math.floor(Math.random() * 20)))
  return arr
}


function testRingBufferExactBoundary() {
  const snapshots = []
  for (let i = 1; i <= MAX_HISTORY; i++) snapshots.push({ name: `s-${i}` })
  assert("ring: exactly MAX_HISTORY stays", snapshots.slice(-MAX_HISTORY).length === MAX_HISTORY)
  assert("ring: first element still s-1", snapshots.slice(-MAX_HISTORY)[0].name === "s-1")

  snapshots.push({ name: "s-11" })
  const pruned = snapshots.slice(-MAX_HISTORY)
  assert("ring: +1 prunes oldest", pruned[0].name === "s-2")
  assert("ring: +1 keeps newest", pruned[pruned.length - 1].name === "s-11")

  // Push 5 more
  for (let i = 12; i <= 15; i++) snapshots.push({ name: `s-${i}` })
  const pruned2 = snapshots.slice(-MAX_HISTORY)
  assert("ring: +5 total prunes to 10", pruned2.length === MAX_HISTORY)
  assert("ring: oldest is s-6", pruned2[0].name === "s-6")
  assert("ring: newest is s-15", pruned2[MAX_HISTORY - 1].name === "s-15")
}
testRingBufferExactBoundary()


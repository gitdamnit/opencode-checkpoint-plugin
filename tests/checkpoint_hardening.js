// Integrity 1: Atomic write with temp + rename
async function testAtomicWriteIntegrity() {
  const dir = join(tmpdir(), "cp-atomic-" + Date.now())
  await mkdir(dir, { recursive: true })
  const file = join(dir, "checkpoint.json")

  const data = { version: 2, updatedAt: new Date().toISOString(), task: { title: "atomic test" } }
  const tmp = file + ".tmp." + Date.now()
  await writeFile(tmp, JSON.stringify(data))
  await rename(tmp, file)

  const loaded = JSON.parse(await readFile(file, "utf8"))
  assert("integrity: atomic write valid", loaded.task.title === "atomic test")

  // Temp file should NOT exist
  let tempExists = false
  try { await readFile(tmp); tempExists = true } catch {}
  assert("integrity: temp file cleaned", !tempExists)

  await rm(dir, { recursive: true, force: true })
}
await testAtomicWriteIntegrity()

// Integrity 2: Partial write (simulated crash) doesn't corrupt
async function testPartialWriteSafety() {
  const dir = join(tmpdir(), "cp-partial-" + Date.now())
  await mkdir(dir, { recursive: true })
  const file = join(dir, "checkpoint.json")

  // Write valid data first
  await writeFile(file, JSON.stringify({ version: 2, updatedAt: new Date().toISOString(), task: { title: "original" } }))

  // Simulate crash mid-write by writing partial content directly (bypassing atomic)
  await writeFile(file, '{"version": 2, "updatedAt": "2026-01-01", ')

  // Read should succeed (or return null) but not crash
  let loaded = null
  try { loaded = JSON.parse(await readFile(file, "utf8")) } catch {}
  assert("integrity: partial write returns null", loaded === null)

  // Clean temp files if any
  await rm(dir, { recursive: true, force: true })
}
await testPartialWriteSafety()

// Integrity 3: Temp file collision on rapid saves
async function testTempFileUniqueness() {
  const dir = join(tmpdir(), "cp-rapid-" + Date.now())
  await mkdir(dir, { recursive: true })
  const file = join(dir, "checkpoint.json")

  const saves = []
  for (let i = 0; i < 20; i++) {
    const tmp = file + ".tmp." + Date.now()
    saves.push(tmp)
    await writeFile(tmp, JSON.stringify({ version: 2, updatedAt: new Date().toISOString(), task: { title: `save-${i}` } }))
    await rename(tmp, file).catch(() => {})
  }

  const loaded = JSON.parse(await readFile(file, "utf8"))
  assert("integrity: rapid saves produce valid file", loaded.version === 2)
  assert("integrity: last save wins", loaded.task.title.startsWith("save-"))

  // All temp files should be cleaned
  for (const tmp of saves) {
    let exists = true
    try { await readFile(tmp) } catch { exists = false }
    if (exists) {
      assert(`integrity: temp file ${tmp} should be gone`, false)
    }
  }

  await rm(dir, { recursive: true, force: true })
}
await testTempFileUniqueness()


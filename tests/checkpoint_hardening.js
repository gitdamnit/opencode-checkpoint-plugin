// Security 1: Prototype pollution prevention
function testPrototypePollution() {
  const malicious = { __proto__: { polluted: true } }
  const result = deepMerge(null, malicious)
  assert("prototype pollution: polluted not set on result", !result.polluted)
  const obj = {}
  assert("prototype pollution: Object prototype not polluted", !obj.polluted)
}
testPrototypePollution()


// Edge 9: Concurrent read/write simulation
function testTOCTOUPattern() {
  const original = { name: "target", savedAt: new Date().toISOString(), data: { version: 2, updatedAt: new Date().toISOString(), task: { title: "target" } } }
  const newEntry = { name: "new", savedAt: new Date().toISOString(), data: { version: 2, updatedAt: new Date().toISOString(), task: { title: "new" } } }

  // Simulate the bug: load history, another write happens, load history again
  let history = { snapshots: [original] }
  const preRestore = [...history.snapshots]
  // Simulate concurrent write
  history.snapshots.push(newEntry)
  // Now re-read (second load) — we got a different snapshot set
  const targetFromSecondRead = history.snapshots.find(s => s.name === "target")
  assert("edge: TOCTOU — target still findable", targetFromSecondRead.name === "target")
  assert("edge: TOCTOU — history has extra entry", history.snapshots.length === 2)
}
testTOCTOUPattern()


// Test 20: Message counter boundary conditions
function testMessageCounterBoundaryConditions() {
  let counter = 0

  // 4 messages — should NOT trigger
  for (let i = 0; i < 4; i++) counter++
  assert("4 messages below threshold", counter < AUTOSAVE_EVERY_N_MESSAGES)

  // 5th message — should trigger
  counter++
  assert("5 messages hits threshold", counter >= AUTOSAVE_EVERY_N_MESSAGES)

  // Reset and verify
  counter = 0
  assert("reset works", counter === 0)

  // 6 messages — should still trigger at 5
  for (let i = 0; i < 6; i++) counter++
  assert("6 messages exceeds threshold", counter >= AUTOSAVE_EVERY_N_MESSAGES)
}

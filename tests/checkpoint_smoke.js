// Test 15: History pruning edge cases
function testHistoryPruningEdgeCases() {
  // Exactly 10 entries — no pruning
  const exactly10 = []
  for (let i = 1; i <= 10; i++) {
    exactly10.push({ name: `e-${i}` })
  }
  const pruned10 = exactly10.slice(-MAX_HISTORY)
  assert("10 entries stays at 10", pruned10.length === 10)
  assert("10 entries keeps first", pruned10[0].name === "e-1")

  // 11 entries — prunes 1
  const eleven = []
  for (let i = 1; i <= 11; i++) {
    eleven.push({ name: `el-${i}` })
  }
  const pruned11 = eleven.slice(-MAX_HISTORY)
  assert("11 entries prunes to 10", pruned11.length === 10)
  assert("11 entries drops e-1", pruned11[0].name === "el-2")

  // 20 entries — prunes 10
  const twenty = []
  for (let i = 1; i <= 20; i++) {
    twenty.push({ name: `t-${i}` })
  }
  const pruned20 = twenty.slice(-MAX_HISTORY)
  assert("20 entries prunes to 10", pruned20.length === 10)
  assert("20 entries keeps t-11 as oldest", pruned20[0].name === "t-11")
  assert("20 entries keeps t-20 as newest", pruned20[9].name === "t-20")
}
testHistoryPruningEdgeCases()

